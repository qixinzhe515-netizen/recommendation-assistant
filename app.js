function hasMatch(items, value) {
  return Boolean(value && items?.includes(value));
}

function textHits(items, text) {
  return (items || []).filter((item) => text.includes(item) || item.includes(text)).length;
}

function scoreProduct(product, analysis, profile) {
  const text = `${analysis.raw} ${analysis.need.label} ${analysis.need.category}`;
  let score = 0;
  const matched = [];

  const keywordHits = textHits(product.keywords, text);
  if (keywordHits) {
    score += keywordHits * 18;
    matched.push("需求关键词匹配");
  }

  if (hasMatch(product.categories, analysis.need.category)) {
    score += 24;
    matched.push("品类匹配");
  }

  if (hasMatch(product.people, analysis.person.type) || hasMatch(product.people, analysis.person.label)) {
    score += 18;
    matched.push("使用对象匹配");
  }

  if (hasMatch(product.scenes, analysis.scenario)) {
    score += 12;
    matched.push("使用场景匹配");
  }

  if (hasMatch(product.budgetFit, profile.budget)) {
    score += 10;
    matched.push("预算方式匹配");
  }

  if (hasMatch(product.priorityFit, profile.priority)) {
    score += 10;
    matched.push("核心偏好匹配");
  }

  if (hasMatch(product.styleFit, profile.style)) {
    score += 8;
    matched.push("推荐风格匹配");
  }

  if (product.complexity === profile.complexity) {
    score += 8;
    matched.push("复杂度匹配");
  }

  if (product.risk === profile.risk) {
    score += 7;
    matched.push("尝新程度匹配");
  }

  if (hasMatch(product.taste, profile.taste)) {
    score += 6;
    matched.push("审美感觉匹配");
  }

  if (hasMatch(product.innerNeed, profile.innerNeed)) {
    score += 7;
    matched.push("内心需求匹配");
  }

  if (profile.hobby && textHits(product.keywords, profile.hobby)) {
    score += 5;
    matched.push("兴趣相关");
  }

  if (hasMatch(product.avoidRisks, profile.avoid)) {
    score += 6;
    matched.push("避开雷点");
  }

  return { product, score, matched };
}

function isDirectlyRelatedProduct(product, analysis) {
  const text = `${analysis.raw} ${analysis.need.label} ${analysis.need.category}`;
  return textHits(product.keywords, text) > 0 || hasMatch(product.categories, analysis.need.category);
}

function recommendationSet(analysis) {
  const profile = state.profile || {};
  const candidates =
    analysis.need.mode === "明确商品型"
      ? productCatalog.filter((product) => isDirectlyRelatedProduct(product, analysis))
      : productCatalog;
  const ranked = (candidates.length ? candidates : productCatalog)
    .map((product) => scoreProduct(product, analysis, profile))
    .sort((a, b) => b.score - a.score);
  const winner = ranked[0].score > 0 ? ranked[0] : scoreProduct(productCatalog[0], analysis, profile);
  const product = winner.product;
  const profileReasons = profileReason();

  return {
    best: product.name,
    intro: product.reasons[0] || "适合当前需求的稳妥选择",
    platform: product.platform,
    price: product.price,
    query: product.query,
    image: productImage(product),
    purchaseUrl: platformSearchUrl(product.platform, product.query),
    reasons: [...product.reasons, ...profileReasons, `当前匹配依据：${winner.matched.slice(0, 4).join("、") || "基础需求匹配"}`],
  };
}

