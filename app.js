function platformSearchUrl(platform, query) {
  const keyword = encodeURIComponent(query);
  if (platform === "京东") return `https://search.jd.com/Search?keyword=${keyword}`;
  if (platform === "淘宝" || platform === "天猫") return `https://s.taobao.com/search?q=${keyword}`;
  if (platform === "拼多多") return `https://mobile.yangkeduo.com/search_result.html?search_key=${keyword}`;
  return `https://www.baidu.com/s?wd=${keyword}`;
}

